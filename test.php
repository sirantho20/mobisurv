$phar = new PharData('/var/www/mobisurv.tar');
$phar->extractTo('tmp',NULL,true);

copyr('tmp/mobisurv','tmp');

function copyr($source, $dest){
    if(is_dir($source)) {
        $dir_handle=opendir($source);
        while($file=readdir($dir_handle)){
            if($file!="." && $file!=".."){
                if(is_dir($source."/".$file)){
                    @mkdir($dest."/".$file);
                    copyr($source."/".$file, $dest."/".$file);
                } else {
                    copy($source."/".$file, $dest."/".$file);
                }
            }
        }
        closedir($dir_handle);
    } else {
        copy($source, $dest);
    }
}

